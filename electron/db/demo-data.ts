import crypto from 'node:crypto';

export interface DemoTransaction {
  id: string;
  amount: number;
  description: string;
  date: Date;
  currency: string;
  merchant: string | null;
  status: string;
  accountId: string;
  bankConnectionId: string | null;
  metadata: string | null;
  categoryId: number | null;
}

export const DEMO_ACCOUNT_ID = 'demo-account-001';

function daysAgo(n: number): Date {
  const d = new Date();
  d.setDate(d.getDate() - n);
  d.setHours(12, 0, 0, 0);
  return d;
}

function rid(): string {
  return crypto.randomUUID();
}

function tx(amount: number, description: string, days: number, merchant: string | null): DemoTransaction {
  return { id: rid(), amount, description, date: daysAgo(days), currency: 'GBP', merchant, status: 'cleared', accountId: DEMO_ACCOUNT_ID, bankConnectionId: null, metadata: null, categoryId: null };
}

